    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/AirlineDB","root","rootuser@1234");
            pre=con.prepareStatement("UPDATE AirlineDB.Customer set firstName=?, lastName=?, passport=?, nic=?, address=?, dob=?, gender=?, Contact=? WHERE idCustomer = ?");
            
            String id=CustomerID.getText();
            String Fname=fname.getText();
            String Lname=lname.getText();
            String Passport=passportID.getText();
            String Nic=nicID.getText();
            String Address=address.getText();
            String Gender=gender.getText();
            String Dob=dob.getText();
            String Contact=contact.getText();
            
            if(Contact.length()>=11)
            {
                JOptionPane.showMessageDialog(null,"Please Enter a Valid contact number");
            }
            else
            {
                pre.setString(1,Fname);
                pre.setString(2,Lname);
                pre.setString(3,Passport);
                pre.setString(4,Nic);
                pre.setString(5,Address);
                pre.setString(6,Dob);
                pre.setString(7,Gender);
                pre.setString(8,Contact);
                pre.setString(9,id);
                
                pre.executeUpdate();   // Updating the badabase table
            
                JOptionPane.showMessageDialog(null,"Registration Updated");   // Showing final mesage
            }
            
            
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


