    // Search Button
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            
            String id=CustomerID.getText();
            
            // Connect with server to fetch data
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/AirlineDB","root","rootuser@1234");
            //System.out.println("Connected");  // To check if database is connected
            
            pre=con.prepareStatement("SELECT * FROM AirlineDB.Customer WHERE idCustomer = ?");
            pre.setString(1, id);   // Assigning customer Id to pre
            ResultSet rs=pre.executeQuery();
            //rs.next();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(null,"User Not Found");
            }
            else
            {
                String Fname=rs.getString("firstName");
                String Lname=rs.getString("lastName");
                String Passport=rs.getString("passport");
                String Nic=rs.getString("nic");
                String Address=rs.getString("address");
                String Dob=rs.getString("dob");
                String Gender=rs.getString("gender");
                String Contact=rs.getString("contact");
                
                // Now we show the fetched data
                fname.setText(Fname);
                lname.setText(Lname);
                passportID.setText(Passport);
                nicID.setText(Nic);
                address.setText(Address);
                dob.setText(Dob);
                gender.setText(Gender);
                contact.setText(Contact);
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

