    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String Source=source.getSelectedItem().toString().trim();
        String Destination=destination.getSelectedItem().toString().trim();
        
        try {
            // Now connect the database
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/AirlineDB","root","rootuser@1234");
            pre=con.prepareStatement("SELECT * FROM flight WHERE Source=? AND Destination=?");
            pre.setString(1, Source);  // Find this source in database
            pre.setString(2, Destination);    //// Find this destination in database
            ResultSet rs=pre.executeQuery();
            
            ResultSetMetaData rsm=rs.getMetaData();
            int col;
            col=rsm.getColumnCount();
            
            DefaultTableModel Df= (DefaultTableModel)jTable1.getModel();   // object for table in our frame
            Df.setRowCount(0);
            
            while(rs.next())   // while it is true
            {
                Vector v2=new Vector();
                
                for(int i=1; i<=col; i++)
                {
                    v2.add(rs.getString("FlightID"));
                    v2.add(rs.getString("FlightName"));
                    v2.add(rs.getString("Source"));
                    v2.add(rs.getString("Destination"));
                    v2.add(rs.getString("Date"));
                    v2.add(rs.getString("DepartureTime"));
                    v2.add(rs.getString("ArrivalTime"));
                    v2.add(rs.getString("FlightCharge"));
                }
                
                Df.addRow(v2);   // Adding fetched columns in current row
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Ticket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

