    // Function to Automatically generate Ticket Number
    public void autoId()
    {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/AirlineDB","root","rootuser@1234");
            //System.out.println("Connected");  // To check if database is connected
            
            Statement s=con.createStatement();  // Craeting a Statement to print in the Screen
            ResultSet rs=s.executeQuery("SELECT MAX(TicketNo) FROM ticket");   // Selecting maximum from ticketNumber
            rs.next();
            if(rs.getString("MAX(TicketNo)")==null)
            {
                TicketNumber.setText("TCN001X");
            }
            else
            {
                long id=Long.parseLong(rs.getString("MAX(TicketNo)").substring(3,rs.getString("MAX(TicketNo)").length()-1));
                id++;   // Incremented mi Id
                TicketNumber.setText("TCN"+String.format("%03d", id)+"X");  // String format print 000+id->002, 003,...
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Add_Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
